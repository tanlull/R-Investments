### Data analysis with built-in functions

### 1. 10 day close price of KTB is given.

ktb <- c(19.7, 19.9, 20.0, 19.7, 19.8, 19.6, 19.5, 19.5, 19.4, 19.3)

### 2. Find an average of 10-day close price and name it as 'ktb.mean'.

ktb.mean <- mean(ktb)


### 3. Find a standard deviation of 10-day close price and name it as 'ktb.sd'.

ktb.sd   <- sd(ktb)


### 4. Find the minimum and maximum close price in 10 days and name them as 'ktb.min' and 'ktb.max'.

ktb.min  <- min(ktb)

ktb.max  <- max(ktb)



